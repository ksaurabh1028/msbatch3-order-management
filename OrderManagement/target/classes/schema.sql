CREATE TABLE SL_ORDERS(
  ID BIGINT AUTO_INCREMENT PRIMARY KEY,
  NAME VARCHAR NOT NULL,
  TOTAL_AMOUNT DECIMAL(20, 2)
);

CREATE TABLE SL_ITEMS(
  ID BIGINT AUTO_INCREMENT PRIMARY KEY,
  ORDER_ID BIGINT,
  NAME VARCHAR NOT NULL,
  QUANTITY BIGINT NOT NULL,
  PRICE DECIMAL(20, 2) NOT NULL,
  AMOUNT DECIMAL(20, 2)
);